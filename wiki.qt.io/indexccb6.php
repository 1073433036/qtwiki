<h1><span class="mw-headline" id="WinRT_Port_Journal">WinRT Port Journal</span></h1>
<p>This page offers a place for thoughts, status reports and current issues connected with the Windows Runtime port of Qt (non desktop Windows (Phone) 8 applications). Feel free to add your topic and give some insight to your work.
</p><p><br />
</p>
<h2><span class="mw-headline" id="Content">Content</span></h2>
<h3><span class="mw-headline" id="2013-03-19_-_Maurice_Kalinowski_-_Frameworks">2013-03-19 - Maurice Kalinowski - Frameworks</span></h3>
<p>Two weeks ago I had the chance to look at some parts of the Windows Runtime port again and now I have some time to write about the outcome.
One of the tedious items when working on the port is that all applications have to be "pre-packaged" to be run on the host system. As this is not really a cross-compile environment, where you need to deploy an application and its dependees to a device, it feels weird. On the other hand it reflects the real world scenario pretty well, which helps you as an app developer so that you do not forget to put items into the final package.
So right now the test build needs to look like this:
</p>
<ul>
<li> App.exe
</li>
<li> AppManifest.XML
</li>
<li> QtCore5d.dll
</li>
<li> Platform plugin…
</li>
<li> …
</li>
</ul>
<p>That implies that you need to copy Qt every time you want to test something. While this is not a problem for an application developer, who copies Qt once, doing that while developing on Qt is time consuming and error-prone.
Browsing through the documentation I found the following option to be interesting: <a rel="nofollow" class="external text" href="http://msdn.microsoft.com/en-us/library/windows/apps/br211439.aspx">Framework </a>
</p><p>There is not much documentation about it, but what you basically can do is to create a "Framework bundle package" and install that. Applications can then specify in their manifest that they depend on it like documented <a rel="nofollow" class="external text" href="http://msdn.microsoft.com/en-us/library/windows/apps/br211428.aspx">here</a>
</p><p>There is no real information available how we can deploy that to a store, but what it basically would provide is something similar to Ministro on Android, meaning a Qt delivery package, which only needs to be downloaded once. You can check on your system, which frameworks are installed by calling "Get-AppXPackage" on the Windows Power Shell. For each package there is a section called "IsFramework". Parsing through that you will figure out that only the C-runtime is deployed currently as a framework, which proves that there is no documentation for good reason.
</p><p>First let me describe how you can create a framework and how you depend on it before I will talk about the problems.
</p><p>To prepare packaging you will need a certificate to sign your framework, you create it like that (in an Admin shell[[Image:|Image:]]):
</p>
<div dir="ltr" class="mw-geshi mw-code mw-content-ltr"><div class="cpp-qt source-cpp-qt"><pre class="de1"> MakeCert <span class="sy0">/</span>n <span class="st0">&quot;CN=QtProject, O=QtProject, L=Berlin, S=Berlin, C=Germany&quot;</span> <span class="sy0">/</span>r <span class="sy0">/</span>h <span class="nu0">0</span> <span class="sy0">/</span>eku <span class="st0">&quot;1.3.6.1.5.5.7.3.3,1.3.6.1.4.1.311.10.3.13&quot;</span> <span class="sy0">/</span>e <span class="nu8">06</span><span class="sy0">/</span><span class="nu8">06</span><span class="sy0">/</span><span class="nu0">2013</span> <span class="sy0">/</span>sv qtkey.<span class="me1">pvk</span> qtkey.<span class="me1">cer</span></pre></div></div>
<p>Write down the company specification, you will need it again at a later step.
Now let's create the pfx file, again you need to be admin:
</p>
<div dir="ltr" class="mw-geshi mw-code mw-content-ltr"><div class="cpp-qt source-cpp-qt"><pre class="de1">Pvk2Pfx <span class="sy0">/</span>pvk qtkey.<span class="me1">pvk</span> <span class="sy0">/</span>pi <span class="sy0">&lt;</span>password<span class="sy0">&gt;</span> <span class="sy0">/</span>spc qtkey.<span class="me1">cer</span> <span class="sy0">/</span>pfx qtkey.<span class="me1">pfx</span> <span class="sy0">/</span>po <span class="sy0">&lt;</span>password<span class="sy0">&gt;&lt;</span>code<span class="sy0">&gt;</span>
&#160;
<span class="sy0">&lt;</span>password<span class="sy0">&gt;</span> specifies the password you gave to the certificate in the first step.
<span class="me1">Following</span> we will create the framework bundle and install it to the <span class="kw3">system</span><span class="sy0">:</span>
&#160;
<span class="co2"># Move all files you need to a directory where you do the packaging</span>
<span class="co2"># Create your ApplicationManifest.xml file and remember to specify the same company details as in the certificate above. Otherwise signing will fail!</span>
<span class="co2"># Create an framework package:</span></pre></div></div>makeappx pack /d package /h SHA256 /p qt5.appx&lt;/code&gt;
<ol>
<li> Sign the package: <div dir="ltr" class="mw-geshi mw-code mw-content-ltr"><div class="cpp-qt source-cpp-qt"><pre class="de1">SignTool sign <span class="sy0">/</span>a <span class="sy0">/</span>v <span class="sy0">/</span>fd SHA256 <span class="sy0">/</span>f qtkey.<span class="me1">pfx</span> <span class="sy0">/</span>p <span class="sy0">&lt;</span>password<span class="sy0">&gt;</span> qt5.<span class="me1">appx</span><span class="sy0">&lt;</span>code<span class="sy0">&gt;</span>
</li>
</ol>
<span class="co2"># Add yourself to the trusted area, otherwise Windows does not allow to side load the framework:</span></pre></div></div>Certutil -addStore TrustedPeople qtkey.cer&lt;/code&gt;
<ol>
<li> Finally install the package to the system: <div dir="ltr" class="mw-geshi mw-code mw-content-ltr"><div class="cpp-qt source-cpp-qt"><pre class="de1">Add<span class="sy0">-</span>AppxPackage <span class="nu18">.5</span>.<span class="me1">appx</span><span class="sy0">&lt;</span>code<span class="sy0">&gt;</span>
</li>
</ol>
<p>&#160;
So<span class="sy0">,</span> we have Qt5 installed as a framework<span class="sy0">,</span> now what? To make use of that in the application<span class="sy0">,</span> your application needs to specify <span class="kw5">Qt</span> as a dependency in the manifest. <span class="me1">Please</span> note that you have to specify the minimum version correctly<span class="sy0">,</span> you cannot just depend on Qt5 in general.
<span class="me1">In</span> <span class="kw1">case</span> you want to update the Qt5 package you must update the <span class="kw5">Qt</span> Version number in the manifest. <span class="me1">Windows</span> does not allow to just exchange the package<span class="sy0">!</span> Afterwards call makeappx<span class="sy0">/</span>SignTool<span class="sy0">/</span>Add<span class="sy0">-</span>Appxpackage as described above. <span class="me1">As</span> you specified the minimum version in your application<span class="sy0">,</span> it will automatically use that.
&#160;
<span class="me1">That</span> is already the first limitation when working with frameworks. <span class="me1">In</span> <span class="kw1">case</span> you <span class="kw3">remove</span> the application<span class="sy0">,</span> <span class="kw5">Qt</span> will be deleted as well. <span class="me1">As</span> soon as no application depends on a framework<span class="sy0">,</span> it is gone. <span class="me1">Luckily</span> the install steps are the same <span class="kw1">for</span> first install and update install. <span class="me1">Hence</span> you will not notice <span class="kw1">this</span> fact most of the <span class="kw3">time</span>.
&#160;
<span class="me1">Also</span> note that compared to application packages you cannot use <span class="st0">&quot;-Register&quot;</span>. <span class="me1">You</span> have to fully package<span class="sy0">,</span> sign and install <span class="kw5">Qt</span><span class="sy0">,</span> there is no development version or such. <span class="me1">However</span><span class="sy0">,</span> debugging into the framework works.
&#160;
<span class="me1">So</span> there are already a lot of steps required to use <span class="kw5">Qt</span> as a framework<span class="sy0">,</span> but still we are in a scenario we wanted to have. <span class="me1">Exchanging</span> <span class="kw5">Qt</span> is simple and we could run the test apps against that without requiring to reinstall them all the <span class="kw3">time</span>. <span class="me1">Let</span> us now go into further downsides of the frameworks<span class="sy0">:</span>
&#160;
What I have been curious about is how you parse the content of a framework. <span class="me1">To</span> summarize<span class="sy0">:</span> you don<span class="st0">'t! You can only take what is inside the root directory and then you can also have libraries there only. Those libraries are then added to the resolving order for dependencies.
So, if your app bundle depends on the Qt5 framework, when you launch your app the system will figure it requires for instance Qt5Core. After not finding it in the app bundle, it checks the Qt5 framework, locates it in the root folder and resolves it. For libraries everything is fine. But what about plugins? You cannot specify to search inside &quot;platforms&quot; inside the Qt5 framework. 
&#160;
What you could consider is frameworks for plugins. Then your app manifest could specify the plugins to use: &quot;jpeg image handler, sql database, winrt platform plugin&quot;. It could actually simplify deployment, but it would cause troubles for startup times etc. Remember that all dependencies are in the root directory, either directory or mapped. So if the plugins are scanned it scans through all dlls in that directory including Qt libraries, different plugins and fitting plugins.
&#160;
So you could still bundle the plugins inside the application package and have the Qt libraries inside the framework. This might work as long as we assume there is no behavior difference between different versions, nor compatibility breaks. Also, it does not help in our initial problem, meaning to speed up Qt development itself.
&#160;
The result of this far too long entry? Frameworks seem to be an interesting approach, but they do not fulfill our needs for development on Qt, neither for application developers using Qt. At least right now, documentation is lacking, public information is basically non-existent. Maybe Microsoft still experiments with it…
&#160;
=== 2013-03-14- Oliver Wolff - Handling keys the &quot;WinRT way&quot; ===
After making Wiggly wiggle on my Windows 8 machine the first issue which came clear was that keyboard input does not work at all in the port'</span>s current state. <span class="me1">The</span> initial commit barely did any handling of the pressed keys and just created basic key events which were sent <span class="kw2">using</span> QWindowSystemInterface. <span class="me1">Unfortunately</span> <span class="kw1">this</span> was not event enough to get some text into the line edit from the example above.
<span class="me1">Using</span> the desktop Windows implementation as a base <span class="kw1">for</span> the work seemed to be a good idea so that was the starting point <span class="kw1">for</span> the <span class="kw1">new</span> key mapper.
<span class="me1">Unfortunately</span> Microsoft decided to <span class="kw3">remove</span> some quite important functions from the WinRT API <span class="br0">&#40;</span>e.<span class="me1">g</span>. <span class="me1">GetKeyboardState</span><span class="sy0">,</span> GetKeyState<span class="sy0">,</span> MapVirtualKey<span class="sy0">,</span> ToUnicode<span class="sy0">,</span> GetKeyboardLayout <span class="kw1">for</span> example<span class="br0">&#41;</span>. <span class="me1">Some</span> of these can be worked around quite easily. <span class="me1">The</span> keyboard state <span class="kw1">for</span> example can be kept in sync manually every <span class="kw3">time</span> a key event occurs. <span class="me1">On</span> the other hand functions which convert <span class="kw2">virtual</span> key and scancode to the actual unicode character are not as easy to replace. <span class="me1">As</span> the outcome of these functions is tightly coupled to the current keyboard layout mapping the key combinations to the resulting keys is tedious <span class="kw1">if</span> not impossible. <span class="me1">Even</span> with a function to obtain the current keyboard layout the actual key mapping effort would be huge.
</p>
<span class="me1">All</span> in all we have to find a way<span class="sy0">,</span> to map the pressed keys to an actual character in order to have proper text input. <span class="me1">I</span> looked <span class="kw1">for</span> replacements in the API but could not find any so more research has to be done there.</pre></div></div>

